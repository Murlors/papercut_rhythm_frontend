欣赏陈粉丸的纸艺作品，会让人时刻惊喜又惊讶，惊喜因为当你一层一层往内观察，能够不断发现作品细节的奇妙之处，就好似粉丸在创作时，悄悄地为我们埋藏的彩蛋；惊讶则是因为你会很好奇，眼前这个清瘦的短发少女，这些鬼马又深刻的想法到底都是从哪儿来的呢。用她的话来说，所谓灵感来源并不一定是某一次单独的经历，而是对生活一直累积的观察和热爱。

她构思创作时角度奇特，而这些并不是哗众取宠的猎奇，她亦不愿意过度解读自己的作品，给出所谓的标准答案。她更希望观者可以体会自己是通过长期扎实的探索，深度的思考，再加上无边际的想象力，而呈现的一个无界的宇宙。

从手工书到纸艺装置，一张纸的奇幻世界
<br/>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
小时候的陈粉丸，是个内向的孩子，不爱说话，但喜欢一个人拿着画笔，把家里的墙上画满，妈妈看阻止未果，便将她送到了少年宫学画，没想到这一学也就一直画了下去，考上了广州美术学院的版画系书籍装帧专业，成为了一名艺术生。

与纸张的缘分，大概要从大二的一门选修课"制作手工书"说起。"当时的我对手工书完全没概念，自己也是个连扣子都不会缝的'手作盲'，这门课上，老师给我们介绍了很多国外艺术家的作品。"陈粉丸感觉到，冥冥之中，自己的一扇大门被打开了，"它让你用自己的力量，陈述你所看到的一切。"原来书本，可以有那么多样的形式来表达，简直可以将全世界都折叠进去！

此刻，书本不再是平面的物件，可以是多种材质、立体的、透视的……她的想法每天都受到了冲击，彻底爱上了做手工书这件事。有一段时间，她以每周一本的速度来做手工书，用以"记录每天晚上的感觉"。生活中的任何事物都能够成为她作品的主角，妈妈种的花儿，大自然中的鸟儿，路边的植物，好朋友的故事，甚至班上男生皮肤上的粉刺都成为好素材。热爱观察的她，时时刻刻都能发现生活中的新大陆。
<br/>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;

慢慢地，累积了一定的想法和技术，粉丸不再满足于书籍这个相对平面的载体了。她第一次尝试将书籍立体化，创作出了《镜》这个系列的小型纸艺装置。作品是依附在老花眼镜上的立体书，将书页做成异形的纸雕，花卉、植物、动物和昆虫，组合起来像架在鼻梁上的微观世界，呈现出多层次的视觉效果，好像在诉说着粉丸眼中的世界。这一尝试让粉丸对纸张的艺术塑造性萌生了新的想法，她跳脱出了书本的限制，创作思路也得到了进一步的开阔。

继而，她尝试更为大型的纸艺装置创作，不仅是体积大，而且从创意到呈现手法都更为仔细考究，作品的主题也涉猎更高层次。不变的是，在每件作品里，粉丸仍然保留一些在她看来有温度的细节。譬如她早期纸艺作品中，常见的各种"人体的器官"。听起来很重口味，但粉丸却觉得是非常有趣的元素。就拿"手"来打比方，粉丸说，自己的家庭并不善于口头上爱的表达，父母甚至坦言"不知道自己女儿在干嘛，笼统地说，就是搞艺术的吧。"粉丸却觉得这样的相处方式非常有爱，家人彼此之间一些肢体的触碰让她感受到了父母对自己的爱。"手"对她来说，就是内向情感直接的表达，一种爱的体现，她希望将这一元素介入到自己所有的创作之中。于是我们可以看到，从最近在广州方所展出的作品《不息》（上图）中，宏观看，它是一条巨龙，没有终结又可被分拆解读，是一种处于变与不变之间的生物，它可以无限延长，而如今展出的仅仅是一部分；从微观上看，龙的鳞片里暗藏小人和手臂，至于这手臂的具体含义，粉丸认为不必给出标准答案，让观者根据自身的经历体会感悟，更能传达想象力"不息"的精神，在观展的同时，放任自己的思绪进入到无限宽广的宇宙。

<br/>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
更早些时候，她的一组纸艺作品《灵魂出窍》便已初见倪端。这个作品的灵感来源于她偶尔读到了达·芬奇为了研究人类构造，解剖人体的故事，当解剖进入到最深层的时候，达·芬奇却没有发现人类的灵魂。对于当时笃信人类拥有实体灵魂的年代，这件事造成了强烈的冲击："在最里层，为什么没有灵魂？"粉丸由此产生了一些思考，关于灵魂，关于与自己的对话，关于生命的终结，也许没人见过灵魂，但粉丸却能将它可视化。

"我运用了有颜色的纸张，但这些颜色是在褪化的颜色，慢慢地，颜色会褪去，正如我们的生命终将褪去，仅仅留下纯白色洁净的灵魂。我是想表达在这个信仰缺失的时代里，人与自己的自我观照，以及在微缩的社会中人与人之间的关系。"当时的自己在创作上突破了过往的平面形式，有机会去思考整个纸艺装置在空间中制造一种"在场"，让她非常兴奋和期待。

粉丸的作品具有大胆强烈的个人风格，总是有些脑洞大开的想法能让观者惊叹，这几年，引发了国内外众多美术馆、博物馆的关注，更是登上了TEDx的演讲舞台。从2014年开始，她的作品陆续在瑞士日内瓦、上海、杭州、北京、广州、深圳、青岛等多地进行展览，获得惠普、方所、K11商场等一众商业品牌的邀请合作。</div>
